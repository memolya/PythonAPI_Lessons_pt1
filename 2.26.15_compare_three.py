def new_function():
    try:
        a = float(input())  # Ввод первого числа    #Код вызывает три отдельных input() для ввода чисел (a, b, c).
        b = float(input())  # Ввод второго числа    #Если пользователь не вводит число, это вызывает исключение ValueError, которое обрабатывается в блоке try-except.
                                                    # Программа сообщает пользователю об ошибке, вместо того чтобы выйти с IndexError.
        c = float(input())  # Ввод третьего числа

        if a > c:  # Сравнение первого и третьего числа
            print("Good")
        else:
            print("Bad")
    except ValueError:
        print("Ошибка: пожалуйста, введите числа.")


